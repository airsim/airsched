#include <assert.h>
// STDAIR 
#include <stdair/bom/SegmentDateKey.hpp>
#include <stdair/bom/SegmentDate.hpp>

namespace STDAIR {

  // ////////////////////////////////////////////////////////////////////
  SegmentDate::SegmentDate (const SegmentDateKey& iKey) : BomAbstract (iKey) {
  }
  
  // ////////////////////////////////////////////////////////////////////
  SegmentDate::~SegmentDate () {
  }

  // //////////////////////////////////////////////////////////////////////
  FlightDate& SegmentDate::getFlightDate() const {
    assert (_flightDate != NULL);
    return *_flightDate;
  }
  
  // //////////////////////////////////////////////////////////////////////
  const SegmentDateKey& SegmentDate::getKey() const {
    return dynamic_cast<const SegmentDateKey&> (_key);
  }
  
  // //////////////////////////////////////////////////////////////////////
  void SegmentDate::toStream (std::ostream& ioOut) const {
    ioOut << toString() << std::endl;
  }

  // //////////////////////////////////////////////////////////////////////
  void SegmentDate::fromStream (std::istream& ioIn) {
  }

  // //////////////////////////////////////////////////////////////////////
  std::string SegmentDate::toString() const {
    return describeKey();
  }
    
  // //////////////////////////////////////////////////////////////////////
  const std::string SegmentDate::describeKey() const {
    return _key.toString();
  }

  // //////////////////////////////////////////////////////////////////////
  const std::string SegmentDate::describeShortKey() const {
    return _key.toString();
  }

}

